function todayShanghai(): string {
  return new Intl.DateTimeFormat('en-CA', {
    timeZone: 'Asia/Shanghai', year: 'numeric', month: '2-digit', day: '2-digit',
  }).format(new Date());
}

export function quoteOfDay(): string {
  const d = todayShanghai();
  const epoch = Math.floor(Date.parse(d) / 86400000);
  return QUOTES[((epoch % QUOTES.length) + QUOTES.length) % QUOTES.length];
}

export function dashboardAutoOpenKey(): string {
  return todayShanghai();
}
